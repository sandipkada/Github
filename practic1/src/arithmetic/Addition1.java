package arithmetic;

public class Addition1 {


public static void main(String[]args) {
	int a=40;
	int b=69; 
	int c=50;
	
	System.out.println("value of a+b*c="+(a+b*c));
	System.out.println("value of a-b+c="+(a-b+c));
	System.out.println("value of a*b%c="+(a*b%c));
	System.out.println("value of a/b+c="+(a/b+c));
}
}
